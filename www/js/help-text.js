function getHelpHTML() {
  var html = []

  html.push("<h3>Supply Chain Weather</h3>")

  html.push("<p><strong>Click</strong> on distribution center or retail location")
  html.push("to see detailed information.")

  html.push("<p><strong>Click</strong> on any supply route to see shipments")
  html.push("along that route and any corresponding weather alerts.")

  return html.join("\n")
}

